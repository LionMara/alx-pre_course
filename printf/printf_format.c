#include <stdio.h>

/**
 * main - this is jus a main
 *
 * return: 0 or nothing
 */

int main (void)
{
	printf("Character: %c\n", 'c');
	printf("Decimal(base 10) %d\n", 10);
	printf("Exponential:%e\n", 0.005);
	printf("Floating:%f\n", 10.4);
	printf("General format float:%g\n", 19.0);
	printf("integer(base10):%i\n", 10);
	printf("Octal(base 8)%o\n", 34);
	printf("String:%s\n", "String");
	printf("Unisigned:%u\n", 10);
	printf("Hexadecimal: %x\n", 15);
	return (0);
}
